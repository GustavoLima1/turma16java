package entidades;

public class Eletronico {
	
	public String nome;
	public boolean funcionando;
	public double preço;
	public String cor;
	
	public Eletronico(String nome, boolean funcionando, double preço, String cor) {
		super();
		this.nome = nome;
		this.funcionando = funcionando;
		this.preço = preço;
		this.cor = cor;
	}
	
}
